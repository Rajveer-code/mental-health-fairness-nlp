"""
integrated_gradients.py
───────────────────────
Integrated Gradients (IG) attribution as a faithfulness robustness check
for the gradient saliency results reported in Table 8 of the manuscript.

Addresses GAP 3 from the JBI revision: gradient saliency has known
faithfulness limitations (Kindermans et al., 2019). IG (Sundararajan et al.,
2017) integrates gradients along a straight path from a zero-embedding
baseline to the input, providing attribution scores that satisfy the
completeness and sensitivity axioms. If IG produces the same near-zero
Jaccard similarity as gradient saliency, the near-zero finding is robust
across attribution methods — converting a "limitation" into a "robustness
check." If they disagree, both results are reported honestly.

Inputs
------
outputs/models/{model}/               — seed=42 checkpoints
outputs/results/shap/{model}_{platform}_{class}_top_words.csv
    Gradient-saliency top-K word sets (generated by code_A4_patch_attribution.py)
data/splits/cross_platform/test_{platform}.csv

Outputs
-------
outputs/results/ig_attribution_results.csv
    Per-(model, class, pair, K) Jaccard similarities for both methods.
    Columns: model, class_name, pair, k, jaccard_gradsal, jaccard_ig, agreement

outputs/results/ig_table8_update.csv
    Table 8 update: one row per (model, class, pair) at K=10,
    with both Grad-Sal J and IG J columns and an agreement flag.

outputs/results/shap/ig_{model}_{platform}_{class}_top_words.csv
    IG top-K word sets (mirrors the gradient saliency format).

Usage
-----
Run from the repository root:
    python src/integrated_gradients.py
    python src/integrated_gradients.py --model bert
    python src/integrated_gradients.py --n-samples 100   # faster testing

Dependencies
------------
Requires captum: pip install captum
Requires outputs/models/{model}/ checkpoints (run train.py first).
Requires outputs/results/shap/ CSVs (run code_A4_patch_attribution.py first).
"""

